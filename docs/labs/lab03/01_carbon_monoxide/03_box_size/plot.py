import numpy as np
import matplotlib.pyplot as plt

def main():
	data = np.loadtxt("data.txt")

	box, homo = data[:,0], data[:,1]

	plt.figure(figsize=(8, 6))
	plt.scatter(box, homo)
	plt.xlabel("Box Size (Ã…)")
	plt.ylabel("HOMO (eV)")
	plt.title("Unit Cell Size Convergence")
	plt.show()

if __name__ == "__main__":
	main()
